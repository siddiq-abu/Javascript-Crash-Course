/// EQUALITY ///

// Double Equals {==} //

let bool = '1' == 1;
console.log(bool);

// NOT EQUAL //
let bol = '1' != 1;
console.log(bol);


// Triple Equals {===} //

let srt = '1' === 1
console.log(srt);

// NOT EQUAL //
let trs = '1' !== 1;
console.log(trs);

/// NOT EQUAL TO ///

// Not Equal to //opposite //
// under boolean //
let str = !true ;
console.log(str);


